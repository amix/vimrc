#include "library.h"

#include <stdlib.h>

/* return the non-null environment value or the default argument */

char	*
mygetenv(char *variable, char *deflt)
{
	char	*value;

	value = getenv(variable);
	if (value == NULL || *value == '\0') {
		return(deflt);
	}
	return(value);
}
